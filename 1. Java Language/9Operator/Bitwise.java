public class Bitwise {
    public static void main(String[] args) {
        
        int x = 7;
        int y = 18;

        System.out.println(x&y);
        System.out.println(x|y);
        System.out.println(x^y);
        System.out.println(~x);
        System.out.println(x<<1);
        System.out.println(y>>2);
    }
}
